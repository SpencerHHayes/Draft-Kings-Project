/*
 * Player Class Created For Future Implementations
 */
public class Player {
	private String name;
	private int worth;

	public int getWorth() {
		return worth;
	}

	public void setWorth(int salary) {
		this.worth = salary;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
